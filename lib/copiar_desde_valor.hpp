LISTA Copiar_Desde_Valor (
  LISTA _lista,
  int   _valor
){

  //////////////////////////////////////////////////////////////

  LISTA subraiz = Encontrar( _lista, _valor );

  //////////////////////////////////////////////////////////////

  if ( Es_Vacia( subraiz ) )

    return NULL;

  //////////////////////////////////////////////////////////////

  else

    return Copiar_Lista( subraiz );

  //////////////////////////////////////////////////////////////

}

