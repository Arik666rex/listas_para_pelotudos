int Buscar_Posicion (
  LISTA _lista,
  int   _valor,
  int   _posicion
){

  //////////////////////////////////////////////////////////////

  if ( Es_Vacia( _lista ) )

    return _posicion = -1;

  //////////////////////////////////////////////////////////////

  else
  if ( _lista->val == _valor )

    return _posicion;

  //////////////////////////////////////////////////////////////

  else

    return Buscar_Posicion( _lista->sig, _valor, _posicion+1 );

  //////////////////////////////////////////////////////////////

}

////////////////////////////////////////////////////////////////

int Posicion (
  LISTA _lista,
  int   _valor
){

  //////////////////////////////////////////////////////////////

  int    posicion = 0;
         posicion = Buscar_Posicion( _lista, _valor, posicion );
  return posicion;

  //////////////////////////////////////////////////////////////

}

