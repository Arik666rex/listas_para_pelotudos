LISTA Copiar_Lista (
  LISTA _lista
){

  //////////////////////////////////////////////////////////////

  if ( Es_Vacia( _lista ) )

    return NULL;

  //////////////////////////////////////////////////////////////

  else {

    LISTA  copia = new NODO;

           copia->val =               _lista->val;
           copia->sig = Copiar_Lista( _lista->sig );

    return copia;

  }

  //////////////////////////////////////////////////////////////

}

